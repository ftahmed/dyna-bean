public class PojoOne {
    private int id;
    private String name;

    public PojoOne() {
        id = 1;
        name = "One";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
