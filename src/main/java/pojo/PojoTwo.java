public class PojoTwo {
    private int id;
    private String name;

    public PojoTwo() {
        id = 2;
        name = "Two";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
